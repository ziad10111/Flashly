export const getReviewCardsForDeck = (deckId: string) => {
  if (deckId === "weak-cards-review") {
    return Object.entries(reviewFlashcardsByDeckId)
      .filter(([id]) => id !== "weak-cards-review")
      .flatMap(([, cards]) => cards.slice(0, 1));
  }

  return reviewFlashcardsByDeckId[deckId] ?? [];
};
